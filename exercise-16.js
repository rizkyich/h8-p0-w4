function graduates(students) {
  // Code disini
  // init empty object to return
  var graduates = {};

  // making class and assign with empty array to store data
  for (var i = 0; i < students.length; i++) {
    var person = students[i];
    graduates[person.class] = [];
  }

  // check all condition and store in object then push into empty array
  for (var j = 0; j < students.length; j++) {
    var gradPerson = students[j];
    if (gradPerson.score > 75) {
      var allGrads = {};

      allGrads.name = gradPerson.name;
      allGrads.score = gradPerson.score;

      graduates[gradPerson.class].push(allGrads);
    }
  }

  return graduates;
}

